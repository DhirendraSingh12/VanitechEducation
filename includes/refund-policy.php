<h2>Refund Policy</h2>

VANITECH EDUCATION PRIVATE LIMITED Refund & Fee Policy  
At VANITECH EDUCATION PRIVATE LIMITED we are committed to providing high-quality computer education and skill development training to all our students. To maintain fairness, discipline, and sustainability of our academic operations, we have established the following strict Refund & Fee Policy.  
By enrolling in any course, training, or program at VANITECH EDUCATION PRIVATE LIMITED, the student and/or guardian acknowledges and agrees to abide by all points mentioned herein.  

<h2>1. General Principles</h2>  
1. All fees paid to VANITECH EDUCATION PRIVATE LIMITED are treated as non-interest bearing deposits and cannot be transferred to any third party.  
2. Fee refund is a privilege, not an entitlement, and will be granted strictly under the conditions mentioned in this policy.  
3. Students are advised to carefully select their course/program before making any payments, as VANITECH EDUCATION PRIVATE LIMITED does not encourage withdrawals once admission is confirmed.  

<h2>2. Admission & Registration Fee</h2>  
• The admission/registration fee is completely non-refundable and non-transferable under any circumstances.  
• This fee covers counseling, seat blocking, resource reservation, and administrative costs incurred by VANITECH EDUCATION PRIVATE LIMITED.  

<h2>3. Course Fee Refund Policy</h2>  
Refund eligibility for tuition/course fees shall be as follows:  
1. Withdrawal before Course Commencement  
 • If a student withdraws admission before the start date of the batch, and a written request is received at least 7 days in advance, only 50% of the tuition fee will be refunded. Registration fee is non-refundable.  
2. Withdrawal after Course Start  
 • Once the course/batch has commenced, no refund of any kind will be made, regardless of whether the student has attended classes or not.  
3. Special Program Fees  
 • Fees paid towards short-term workshops, crash courses, seminars, certifications, and training camps are strictly non-refundable once registration is confirmed.  

<hr>

<h2>4. Student-Initiated Withdrawal</h2>  
• If a student decides to discontinue due to personal reasons, academic difficulty, relocation, or change of interest, VANITECH EDUCATION PRIVATE LIMITED will not issue any refund.  
• In such cases, students may apply for a batch transfer within the first 10 days, subject to approval and availability. Additional charges may apply.  

<h2>5. Institutional-Initiated Withdrawal</h2>  
• If VANITECH EDUCATION PRIVATE LIMITED is required to expel a student due to misconduct, violation of rules, non-payment of fees, or misrepresentation of documents, no refund will be processed under any circumstances.  

<h2>6. Program Cancellation by VANITECH EDUCATION PRIVATE LIMITED (Institute Grounds)</h2>  
• If VANITECH EDUCATION PRIVATE LIMITED cancels a program/course before commencement, students will receive a 100% refund of the course fee (excluding registration fee).  
• If VANITECH EDUCATION PRIVATE LIMITED discontinues a batch/program midway due to unavoidable reasons, a pro-rata credit note will be issued, allowing the student to continue in another batch of the same course.  
• In such cases, cash/online refunds will not be processed; only fee adjustments/credits are granted.  

<h2>7. Payment Terms & Schedule</h2>  
1. All fees must be paid in full by the due date. Late fees will be levied for delayed payments as per VANITECH EDUCATION PRIVATE LIMITED finance policy.  
2. Partial or installment payments are binding commitments. If a student discontinues in between, they remain liable to pay the entire course fee as per enrollment terms.  
3. Non-payment of installments automatically disqualifies refund eligibility.  

<h2>8. Mode & Timeline of Refunds</h2>  
• Approved refunds, if any, will only be made via bank account transfer/cheque/official channel of the student/guardian.  
• The refund process may take up to 30 working days from the date of written approval.  
• Cash refunds are strictly prohibited.  

<h2>9. Force Majeure & Emergencies</h2>  
• VANITECH EDUCATION PRIVATE LIMITED will not be liable to issue refunds in cases of natural disasters, government regulations, pandemics, technical disruptions, or events beyond the institute’s control.  
• In such cases, VANITECH EDUCATION PRIVATE LIMITED may offer alternate learning modes (such as online classes, rescheduled batches, or credit adjustments) instead of refunds.  

<h2>10. Documentation & Procedure for Refund Request</h2>  
1. A written application must be submitted to the Administration Office stating the reason for withdrawal.  
2. The student must attach the original fee receipt, valid ID proof, and supporting documents (if applicable).  
3. The Managing Committee will review requests, and the decision taken by VANITECH EDUCATION PRIVATE LIMITED Management will be final and binding.  

<h2>11. Disclaimer & Authority</h2>  
1. VANITECH EDUCATION PRIVATE LIMITED reserves the right to modify, amend, or update this policy at any time without prior notice.  
2. Any dispute arising from this policy will be subject to the jurisdiction of the local courts where VANITECH EDUCATION PRIVATE LIMITED operates.  
3. The decision of VANITECH EDUCATION PRIVATE LIMITED Management regarding refunds shall be treated as final, conclusive, and binding on all parties.  

<strong>Important Note to Students & Guardians</strong>  
• VANITECH EDUCATION PRIVATE LIMITED is a professional training institute, and maintaining the quality of courses requires advance financial planning, resources, and faculty arrangements.  
• Hence, students are strongly advised to review all course details before enrolling, as fee payments represent a confirmed commitment and cannot be easily reversed.  

